"""Domain Operations — entity management through the Application layer.

Milestone 10: expose existing domain concepts cleanly. Every operation is
pure orchestration of documented aggregate methods plus repository calls —
construct via the domain constructor, mutate via aggregate methods, persist
through repository INTERFACES. No business rules live here: the domain
validates, the repositories persist.

Dependency rule (mission-mandated, strictly downward):

    application -> repositories.interfaces -> domain

This module therefore imports NOTHING from runtime, scheduler,
decision_engine, or learning. Time is injected as a zero-argument callable
(wired to the composition-root Clock in bootstrap) so the one composition-
time decision about time stays where it already lives.

The single application-level guard here is duplicate detection: aggregate
identifiers are freshly generated UUIDs, so identifier collisions cannot
express the user error "I already created this". Uniqueness across a
collection is not expressible inside a single aggregate; checking the store
before creating is an orchestration concern, not a domain rule, and
duplicates surface as ``DuplicateEntityError``.

Habits and Insights are generated by the Learning Engine and remain
READ-ONLY here (mission rule): list/get only, no create, no mutation.
"""

