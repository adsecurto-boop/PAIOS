#: Who suggested a Goal the user created directly (the domain default is
#: "Decision Engine"; a user-entered Goal is suggested — and accepted — by
#: the User, per DOMAIN_MODEL.md Principle 4's acceptance requirement).
GOAL_SUGGESTED_BY_USER = "User"


class OperationsRepositoryProvider(Protocol):
    """The slice of repository access Domain Operations needs.

    Structural contract satisfied by RepositoryFactory; only repository
    INTERFACES are reachable through it. Runtime-only aggregates
    (context windows, recommendations, disturbers) are intentionally
    absent — they are managed by the Scheduler, not by users.
    """

    def users(self) -> UserRepository: ...
    def principles(self) -> PrincipleRepository: ...
    def contexts(self) -> ContextRepository: ...
    def events(self) -> EventRepository: ...
    def projects(self) -> ProjectRepository: ...
    def progress(self) -> ProgressRepository: ...
    def resources(self) -> ResourceRepository: ...
    def knowledge(self) -> KnowledgeRepository: ...
    def reflections(self) -> ReflectionRepository: ...
    def insights(self) -> InsightRepository: ...
    def habits(self) -> HabitRepository: ...
    def goals(self) -> GoalRepository: ...


class DomainOperations:
    """Entity management: orchestration only, one aggregate at a time."""

    def __init__(
        self,
        repositories: OperationsRepositoryProvider,
        now: Callable[[], datetime],
    ) -> None:
        self._repositories = repositories
        self._now = now

    # --- users -----------------------------------------------------------

    def add_user(self, name: str) -> User:
        repository = self._repositories.users()
        self._reject_duplicate(repository, "User", name=name)
        user = User(user_id=UserId.new(), name=name, created_at=self._now())
        repository.save(user)
        return user

    def list_users(self) -> list[User]:
        return self._repositories.users().list()

    def get_user(self, user_id: UserId) -> User:
        return self._repositories.users().get(user_id)

    # --- goals -----------------------------------------------------------

    def add_goal(
        self, user_id: UserId, name: str, description: str = ""
    ) -> Goal:
        """A user-created Goal is suggested by the User and — because the
        user stating it IS the acceptance decision — accepted immediately
        through the documented aggregate method."""
        repository = self._repositories.goals()
        self._reject_duplicate(repository, "Goal", name=name)
        goal = Goal(
            goal_id=GoalId.new(),
            user_id=user_id,
            name=name,
            description=description,
            suggested_by=GOAL_SUGGESTED_BY_USER,
        )
        goal.accept(self._now())
        repository.save(goal)
        return goal

    def list_goals(self) -> list[Goal]:
        return self._repositories.goals().list()

    def get_goal(self, goal_id: GoalId) -> Goal:
        return self._repositories.goals().get(goal_id)

    def accept_goal(self, goal_id: GoalId) -> Goal:
        """Accept an engine-suggested Goal (DOMAIN_MODEL.md Principle 4)."""
        repository = self._repositories.goals()
        goal = repository.get(goal_id)
        goal.accept(self._now())
        repository.update(goal)
        return goal

    def complete_goal(self, goal_id: GoalId) -> Goal:
        return self._set_goal_status(goal_id, GoalStatus.COMPLETED)

    def pause_goal(self, goal_id: GoalId) -> Goal:
        return self._set_goal_status(goal_id, GoalStatus.PAUSED)

    def resume_goal(self, goal_id: GoalId) -> Goal:
        return self._set_goal_status(goal_id, GoalStatus.ACTIVE)

    def _set_goal_status(self, goal_id: GoalId, status: GoalStatus) -> Goal:
        repository = self._repositories.goals()
        goal = repository.get(goal_id)
        goal.change_status(status)
        repository.update(goal)
        return goal

    # --- projects --------------------------------------------------------

    def add_project(
        self, user_id: UserId, name: str, description: str = ""
    ) -> Project:
        """Create a Project together with the one Progress it owns
        (BUSINESS_RULES.md - Progress belongs to exactly one Project)."""
        repository = self._repositories.projects()
        self._reject_duplicate(repository, "Project", name=name)
        project = Project(
            project_id=ProjectId.new(),
            user_id=user_id,
            name=name,
            description=description,
            created_at=self._now(),
        )
        progress = Progress(
            progress_id=ProgressId.new(), project_id=project.project_id
        )
        project.attach_progress(progress.progress_id)
        repository.save(project)
        self._repositories.progress().save(progress)
        return project

    def list_projects(self) -> list[Project]:
        return self._repositories.projects().list()

    def get_project(self, project_id: ProjectId) -> Project:
        return self._repositories.projects().get(project_id)

    def get_project_progress(self, project_id: ProjectId) -> Progress | None:
        project = self._repositories.projects().get(project_id)
        if project.progress_id is None:
            return None
        return self._repositories.progress().get(project.progress_id)

    def update_project_progress(
        self, project_id: ProjectId, completion_percentage: float
    ) -> Progress:
        progress = self.get_project_progress(project_id)
        if progress is None:
            raise ProgressNotAttachedError(
                f"Project {project_id} owns no Progress to update"
            )
        progress.update(
            self._now(), completion_percentage=completion_percentage
        )
        self._repositories.progress().update(progress)
        return progress

    def complete_project(self, project_id: ProjectId) -> Project:
        return self._set_project_status(project_id, ProjectStatus.COMPLETED)

    def pause_project(self, project_id: ProjectId) -> Project:
        return self._set_project_status(project_id, ProjectStatus.PAUSED)

    def resume_project(self, project_id: ProjectId) -> Project:
        return self._set_project_status(project_id, ProjectStatus.ACTIVE)

    def _set_project_status(
        self, project_id: ProjectId, status: ProjectStatus
    ) -> Project:
        repository = self._repositories.projects()
        project = repository.get(project_id)
        project.change_status(status)
        repository.update(project)
        return project

    # --- principles ------------------------------------------------------

    def add_principle(
        self, name: str, category: PrincipleCategory, description: str = ""
    ) -> Principle:
        repository = self._repositories.principles()
        self._reject_duplicate(repository, "Principle", name=name)
        principle = Principle(
            principle_id=PrincipleId.new(),
            name=name,
            description=description,
            category=category,
            created_at=self._now(),
        )
        repository.save(principle)
        return principle

    def list_principles(self) -> list[Principle]:
        return self._repositories.principles().list()

    def get_principle(self, principle_id: PrincipleId) -> Principle:
        return self._repositories.principles().get(principle_id)

    def review_principle(self, principle_id: PrincipleId) -> Principle:
        """A deliberate User review; the Principle itself never changes."""
        repository = self._repositories.principles()
        reviewed = repository.get(principle_id).reviewed(self._now())
        repository.update(reviewed)
        return reviewed

    # --- resources -------------------------------------------------------

    def add_resource(
        self,
        user_id: UserId,
        type: ResourceType,
        current_value: float,
        unit: str,
        negative_allowed: bool = False,
    ) -> Resource:
        repository = self._repositories.resources()
        self._reject_duplicate(
            repository, "Resource", user_id=user_id, type=type
        )
        resource = Resource(
            resource_id=ResourceId.new(),
            user_id=user_id,
            type=type,
            current_value=current_value,
            unit=unit,
            negative_allowed=negative_allowed,
            last_updated=self._now(),
        )
        repository.save(resource)
        return resource

    def list_resources(self) -> list[Resource]:
        return self._repositories.resources().list()

    def get_resource(self, resource_id: ResourceId) -> Resource:
        return self._repositories.resources().get(resource_id)

    def consume_resource(
        self, resource_id: ResourceId, amount: float
    ) -> Resource:
        repository = self._repositories.resources()
        resource = repository.get(resource_id)
        resource.consume(amount, self._now())
        repository.update(resource)
        return resource

    def produce_resource(
        self, resource_id: ResourceId, amount: float
    ) -> Resource:
        repository = self._repositories.resources()
        resource = repository.get(resource_id)
        resource.produce(amount, self._now())
        repository.update(resource)
        return resource

    # --- contexts --------------------------------------------------------

    def add_context(
        self,
        name: str,
        location: str | None = None,
        people: tuple[str, ...] = (),
        emotion: str | None = None,
        trigger: str | None = None,
        reason: str | None = None,
        environment: str | None = None,
        notes: str | None = None,
    ) -> Context:
        repository = self._repositories.contexts()
        self._reject_duplicate(repository, "Context", name=name)
        context = Context(
            context_id=ContextId.new(),
            name=name,
            created_at=self._now(),
            location=location,
            people=people,
            emotion=emotion,
            trigger=trigger,
            reason=reason,
            environment=environment,
            notes=notes,
        )
        repository.save(context)
        return context

    def list_contexts(self) -> list[Context]:
        return self._repositories.contexts().list()

    def get_context(self, context_id: ContextId) -> Context:
        return self._repositories.contexts().get(context_id)

    # --- knowledge -------------------------------------------------------

    def add_knowledge(
        self,
        user_id: UserId,
        domain: str,
        topic: str,
        concept: str,
        project_id: ProjectId | None = None,
        difficulty: str | None = None,
        confidence: float = 0.0,
        source: str | None = None,
    ) -> Knowledge:
        repository = self._repositories.knowledge()
        self._reject_duplicate(
            repository,
            "Knowledge",
            user_id=user_id,
            domain=domain,
            topic=topic,
            concept=concept,
        )
        knowledge = Knowledge(
            knowledge_id=KnowledgeId.new(),
            user_id=user_id,
            domain=domain,
            topic=topic,
            concept=concept,
            project_id=project_id,
            difficulty=difficulty,
            confidence=confidence,
            source=source,
        )
        repository.save(knowledge)
        return knowledge

    def list_knowledge(self) -> list[Knowledge]:
        return self._repositories.knowledge().list()

    def get_knowledge(self, knowledge_id: KnowledgeId) -> Knowledge:
        return self._repositories.knowledge().get(knowledge_id)

    def revise_knowledge(
        self, knowledge_id: KnowledgeId, confidence: float | None = None
    ) -> Knowledge:
        repository = self._repositories.knowledge()
        knowledge = repository.get(knowledge_id)
        knowledge.revise(self._now(), confidence=confidence)
        repository.update(knowledge)
        return knowledge

    def apply_knowledge(self, knowledge_id: KnowledgeId) -> Knowledge:
        repository = self._repositories.knowledge()
        knowledge = repository.get(knowledge_id)
        knowledge.mark_applied()
        repository.update(knowledge)
        return knowledge

    # --- events (read-only listing; the Scheduler owns Event mutation) ----

    def list_events(self) -> list:
        """Milestone 11: a fresh, store-backed Event listing for read-only
        presentation (the Dashboard). Event MUTATION stays exclusively with
        the Scheduler; this is a persistence query, nothing more."""
        return self._repositories.events().list()

    # --- reflections -----------------------------------------------------

    def add_reflection(
        self,
        event_id: EventId,
        facts: str | None = None,
        interpretation: str | None = None,
        root_cause: str | None = None,
        lesson_learned: str | None = None,
        improvement: str | None = None,
        confidence: float | None = None,
    ) -> Reflection:
        """Capture the user's retrospective on a Completed Event.

        Orchestration of documented domain steps: the Event validates that
        reflection is permitted (Completed/Archived, at most one) via
        ``link_reflection``; the Reflection takes its Context Window from
        the Event it interprets (a Reflection cannot exist independently).
        The Event is linked BEFORE anything is persisted so a domain
        rejection leaves the store untouched.
        """
        events = self._repositories.events()
        event = events.get(event_id)
        reflection = Reflection(
            reflection_id=ReflectionId.new(),
            event_id=event.event_id,
            context_window_id=event.context_window_id,
            created_at=self._now(),
            facts=facts,
            interpretation=interpretation,
            root_cause=root_cause,
            lesson_learned=lesson_learned,
            improvement=improvement,
            confidence=confidence,
        )
        event.link_reflection(reflection.reflection_id)
        self._repositories.reflections().save(reflection)
        events.update(event)
        return reflection

    def list_reflections(self) -> list[Reflection]:
        return self._repositories.reflections().list()

    def get_reflection(self, reflection_id: ReflectionId) -> Reflection:
        return self._repositories.reflections().get(reflection_id)

    # --- habits and insights (Learning Engine output; READ-ONLY) ----------

    def list_habits(self) -> list[Habit]:
        return self._repositories.habits().list()

    def get_habit(self, habit_id: HabitId) -> Habit:
        return self._repositories.habits().get(habit_id)

    def list_insights(self) -> list[Insight]:
        return self._repositories.insights().list()

    def get_insight(self, insight_id: InsightId) -> Insight:
        return self._repositories.insights().get(insight_id)

    # --- internals -------------------------------------------------------

    @staticmethod
    def _reject_duplicate(repository, label: str, **criteria: object) -> None:
        """Duplicate detection: a persistence query (find_by attribute
        equality), never a business rule."""
        if repository.find_by(**criteria):
            described = ", ".join(
                f"{attribute}={value!s}"
                for attribute, value in criteria.items()
            )
            raise DuplicateEntityError(
                f"A {label} with {described} already exists"
            )
